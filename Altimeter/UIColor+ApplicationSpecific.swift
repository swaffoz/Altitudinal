import UIKit

extension UIColor {
    class func applicationPurpleColor() -> UIColor {
        return UIColor(red: 99.0/255.0, green: 96.0/255.0, blue: 148.0/255.0, alpha: 1.0)
    }
    
    class func applicationBlueColor() -> UIColor {
        return UIColor(red: 96.0/255.0, green: 132.0/255.0, blue: 148.0/255.0, alpha: 1.0)
    }
    
    class func applicationBrightGreenColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 255.0/255.0, blue: 179.0/255.0, alpha: 1.0)
    }
    
    class func applicationGreenColor() -> UIColor {
        return UIColor(red: 89.0/255.0, green: 201.0/255.0, blue: 168.0/255.0, alpha: 1.0)
    }
    
    class func applicationDullGreenColor() -> UIColor {
        return UIColor(red: 95.0/255.0, green: 184.0/255.0, blue: 157.0/255.0, alpha: 1.0)
    }
    
    class func applicationGrayColor() -> UIColor {
        return UIColor(red: 76.0/255.0, green: 76.0/255.0, blue: 76.0/255.0, alpha: 1.0)
    }
    
    class func applicationLightGrayColor() -> UIColor {
        return UIColor(red: 102.0/255.0, green: 102.0/255.0, blue: 102.0/255.0, alpha: 1.0)
    }
}